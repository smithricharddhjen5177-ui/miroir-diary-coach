export function AppSidebar() {
  const pathname = usePathname()
  const { theme, toggleTheme } = useTheme()

  const navContent = (
    <div className="flex h-full flex-col">
      <div className="px-4 py-5">
        <Link href="/today" className="flex items-center gap-2.5">
          <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-primary text-primary-foreground text-sm font-semibold">
            M
          </div>
          <div>
            <h1 className="text-sm font-semibold leading-tight">Miroir</h1>
            <p className="text-[10px] text-muted-foreground leading-tight">日记教练</p>
          </div>
        </Link>
      </div>

      <Separator />

      <nav className="flex-1 px-3 py-3 space-y-0.5">
        {NAV_ITEMS.map((item) => {
          const isActive =
            item.href === "/today"
              ? pathname === "/today" || pathname === "/"
              : pathname.startsWith(item.href.replace("/current", ""))

          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                "flex items-center gap-3 rounded-md px-3 py-2 text-sm transition-colors",
                "hover:bg-accent hover:text-accent-foreground",
                isActive
                  ? "bg-accent text-accent-foreground font-medium"
                  : "text-muted-foreground"
              )}
            >
              <item.icon className="h-4 w-4 shrink-0" />
              <span className="flex-1">{item.label}</span>
              {item.shortcut && (
                <kbd className="hidden md:inline-flex h-5 items-center gap-0.5 rounded border bg-muted px-1.5 font-mono text-[10px] font-medium text-muted-foreground">
                  ⌘{item.shortcut}
                </kbd>
              )}
            </Link>
          )
        })}
      </nav>

      <Separator />

      <div className="px-3 py-3 space-y-1">
        <Link
          href="/settings"
          className={cn(
            "flex items-center gap-3 rounded-md px-3 py-2 text-sm transition-colors text-muted-foreground",
            "hover:bg-accent hover:text-accent-foreground",
            pathname === "/settings" && "bg-accent text-accent-foreground font-medium"
          )}
        >
          <SettingsIcon className="h-4 w-4 shrink-0" />
          <span>设置</span>
        </Link>

        <button
          onClick={toggleTheme}
          className="flex w-full items-center gap-3 rounded-md px-3 py-2 text-sm transition-colors text-muted-foreground hover:bg-accent hover:text-accent-foreground"
        >
          {theme === "dark" ? (
            <SunIcon className="h-4 w-4 shrink-0" />
          ) : (
            <MoonIcon className="h-4 w-4 shrink-0" />
          )}
          <span>{theme === "dark" ? "浅色模式" : "深色模式"}</span>
        </button>
      </div>
    </div>
  )

  return (
    <>
      {/* Mobile: sheet sidebar */}
      <Sheet>
        <SheetTrigger className="fixed top-3 left-3 z-50 md:hidden h-9 w-9 inline-flex items-center justify-center rounded-md hover:bg-accent transition-colors">
          <MenuIcon className="h-5 w-5" />
        </SheetTrigger>
        <SheetContent side="left" className="w-60 p-0">
          <SheetTitle className="sr-only">导航</SheetTitle>
          {navContent}
        </SheetContent>
      </Sheet>

      {/* Desktop: fixed sidebar */}
      <aside className="hidden md:flex w-56 shrink-0 border-r bg-sidebar text-sidebar-foreground flex-col">
        {navContent}
      </aside>
    </>
  )
}

